    private void tampil() {
        Valid.tabelKosong(tabMode);
        try{  
            file=new File("./cache/masteranjuranmcu.iyem");
            file.createNewFile();
            fileWriter = new FileWriter(file);
            StringBuilder iyembuilder = new StringBuilder();
            ps=koneksi.prepareStatement("select master_kesimpulan_anjuran_mcu.kesimpulan,master_kesimpulan_anjuran_mcu.anjuran from master_kesimpulan_anjuran_mcu order by master_kesimpulan_anjuran_mcu.kesimpulan");
            try {
                rs=ps.executeQuery();
                while(rs.next()){
                    tabMode.addRow(new Object[]{rs.getString(1),rs.getString(2)});
                    iyembuilder.append("{\"Kesimpulan\":\"").append(rs.getString(1)).append("\",\"Anjuran\":\"").append(rs.getString(2)).append("\"},");
                }
            } catch (Exception e) {
                System.out.println("Notifikasi : "+e);
            } finally{
                if(rs!=null){
                    rs.close();
                }
                if(ps!=null){
                    ps.close();
                }
            }
            if (iyembuilder.length() > 0) {
                iyembuilder.setLength(iyembuilder.length() - 1);
                fileWriter.write("{\"masteranjuranmcu\":["+iyembuilder+"]}");
                fileWriter.flush();
            }
            
            fileWriter.close();
            iyembuilder=null;
        }catch(Exception e){
            System.out.println("Notifikasi : "+e);
        }
        LCount.setText(""+tabMode.getRowCount());
    }
    
    private void tampil2() {
        try {
            myObj = new FileReader("./cache/masteranjuranmcu.iyem");
            root = mapper.readTree(myObj);
            Valid.tabelKosong(tabMode);
            response = root.path("masteranjuranmcu");
            if(response.isArray()){
                if(TCari.getText().trim().equals("")){
                    for(JsonNode list:response){
                        tabMode.addRow(new Object[]{
                            list.path("Kesimpulan").asText(),list.path("Anjuran").asText()
                        });
                    }
                }else{
                    for(JsonNode list:response){
                        if(list.path("Kesimpulan").asText().toLowerCase().contains(TCari.getText().toLowerCase())){
                            tabMode.addRow(new Object[]{
                                list.path("Kesimpulan").asText(),list.path("Anjuran").asText()
                            });
                        }
                    }
                }
            }
            myObj.close();
            if(tabMode.getRowCount()==0){
                ps=koneksi.prepareStatement("select master_kesimpulan_anjuran_mcu.kesimpulan,master_kesimpulan_anjuran_mcu.anjuran from master_kesimpulan_anjuran_mcu where master_kesimpulan_anjuran_mcu.kesimpulan like ? order by master_kesimpulan_anjuran_mcu.kesimpulan");
                try {
                    ps.setString(1,"%"+TCari.getText().trim()+"%");
                    rs=ps.executeQuery();
                    while(rs.next()){
                        tabMode.addRow(new Object[]{rs.getString(1),rs.getString(2)});
                    }
                } catch (Exception e) {
                    System.out.println("Notifikasi : "+e);
                } finally{
                    if(rs!=null){
                        rs.close();
                    }
                    if(ps!=null){
                        ps.close();
                    }
                }
            }
        } catch (Exception ex) {
            System.out.println("Notifikasi : "+ex);
        }
        LCount.setText(""+tabMode.getRowCount());
    }

